// Program ini mengilustrasikan bagaiman statemen keluaran bekerja.

#include <iostream>

using namespace std;

int main()
{
    int a, b;
    a = 65; //Baris 1
    b = 78; //Baris 2

    cout << 29 / 4 << endl; //Baris 3
    cout << 3.0 / 2 << endl; //Baris 4
    cout << "Danau Toba.\n"; //Baris 5
    cout << 7 << endl; //Baris 6
    cout << 3 + 5 << endl; //Baris 7
    cout << "3 + 5"; //Baris 8
    cout << endl; //Baris 9
    cout << a << endl; //Baris 10
    cout << "a" << endl; //Baris 11
    cout << (a + 5) * 6 << endl; //Baris 12
    cout << 2 * b << endl; //Baris 13

    return 0;
}